package com.atguigu.ch01;

public class A02ShortAndChar {
    public static void main(String[] args) {
        short s = Short.MAX_VALUE;
        char c = Character.MAX_VALUE;

        int is = s;
        int ic = c;
        System.out.println("is = " + is);
        System.out.println("ic = " + ic);
    }
}
